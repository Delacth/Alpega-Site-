            Constam no portal MEC as funções do técnico de serviços jurídicos, conforme segue: Executa serviços de suporte e apoio técnico-administrativo a escritórios de advocacia, de auditoria jurídica, recursos humanos e departamentos administrativos, bem como cumpre as determinações legais atribuídas a cartórios judiciais e extrajudiciais, executando procedimentos e registros cabíveis. É responsável pelo gerenciamento e pelo arquivo de processos e de documentos técnicos. Presta atendimento ao público. (MEC, 2005)
