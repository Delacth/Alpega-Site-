          <p class="">Técnico jurídico ou Técnico em Serviços Jurídicos é uma pessoa que ajuda um advogado no seu trabalho diário. Esta ajuda pode ser dada tanto na pesquisa como na compilação de informações de âmbito forense que possam facilitar o trabalho do advogado. Fazem buscas de casos passados, recolhem informações e documentos necessários para o bom andamento de um processo. Podem preparar contratos, hipotecas e procurações, facilitando o trabalho de um advogado. Em inglês designa-se paralegal.</p>
          
          <p>O Técnico de Serviços Jurídicos é uma profissão recente sendo ainda pouco conhecida no meio acadêmico e profissional, tendo sido criado em 2005 pela Centro Paula Souzapara atender solicitação do Tribunal de Justiça de São Paulo que necessitava de qualificação dos serventuários que atuavam diretamente na atividade-fim do Poder Judiciário e de formação e capacitação de profissionais para atuarem junto a diversos órgãos, públicos e privados.</p>

